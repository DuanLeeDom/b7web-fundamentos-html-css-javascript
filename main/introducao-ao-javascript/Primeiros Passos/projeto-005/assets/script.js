/* Ponto e vírgula
  O ponto e vírgula (;) em JavaScript é, na maioria dos casos, opcional.
  No entanto, ele é obrigatório quando declaramos mais de uma instrução na mesma linha, como neste exemplo:

  let nome = "Duan"; let idade = 19;

  Além disso, é uma boa prática adotá-lo sempre, pois:
  - Ajuda a evitar ambiguidades em certos casos;
  - Facilita a leitura do código;
  - Cria um bom hábito para outras linguagens onde o ponto e vírgula é obrigatório.
*/

let nome = "Bonieky"; let idade = 90;

console.log(nome);
console.log(idade);
